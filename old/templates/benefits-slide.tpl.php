			<h3>Benefits of Applying &ndash; <strong>Agencies</strong></h3>
			<div>
				<p>Agencies exist to make their clients look good, lending their smarts and savvy to the client’s benefit without the expectation of wide recognition for the successes of their projects. Winning a DfE award gives agencies an opportunity to demonstrate their excellence in a way that transcends specific client work.  The DfE awards offer a way to show potential clients, prospective employees, and the broader design community what your agency is truly capable of and why your approach to designing for experience is a cut above the rest.</p>
			</div>

			<h3>Benefits of Applying &ndash; <strong>In-House Groups</strong></h3>
			<div>
				<p>As experience strategy and design continue to work their way deeper into the fabric of companies of every size and shape, it becomes more acutely obvious which companies know how to truly serve their customers and users. Winning a DfE award is an opportunity to show them that the quality of the experiences you offer is hard-fought and exceptional. It also shows hard-to-find practitioners that your company is somewhere they’d enjoy working, and can be used to continue to build support for experience strategy, research, and design among stakeholders and executives.</p>
			</div>
			
			<h3>Benefits of Applying &ndash; <strong>Individual Practitioners</strong></h3>
			<div>
				<p>Practitioners working in experience-related fields are often overshadowed by the experiences they work so diligently to create. For most of us, being unrecognized is actually a kind of compliment; the experience should be the focus and should be so easy and sensible that people’s attention isn’t drawn away from their goals onto the design itself. A DfE award offers lasting, meaningful recognition for the excellence of your work by your peers and some of the most respected members of the UX community, and can go a long way toward vindicating your tireless advocacy of users, research, design, and experience strategy.</p>
			</div>
			
			<h3>Benefits of Applying &ndash; <strong>Academic Community</strong></h3>
			<div>
				<p>Experience design and technology encompass a set of disciplines that are rapidly growing and in importance, value, and complexity. Winning a DfE award shows current and prospective students and faculty, as well as the broader academic community, that your institution working at the modern forefront, shaping the future of design and technology and enriching the workforce with prepared, experienced new talent.</p>
			</div>
