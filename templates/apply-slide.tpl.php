		<h3>Submitting an Application</h3>
		
		<p>The application process is done online through <a href="javascript:openAppForm()">a form on this site</a>. You'll need to complete a one-time account registration to access the form, and that will allow you to save partially completed applications and/or submit multiple applications under the same account.</p>
		
		<p>While it is possible to create your application entirely in the form, we recommend that you prepare your answers separately in a document you can easily edit and save, and then paste your answers into the form when you’re ready. To help facilitate the process of applying for a Design for Experience award, we’ve created <a href="#" class="open-downloads-form">Word document downloads</a> for each category that contain the category description and all of the application questions. The documents are editable so you can use them to prepare your application question responses before pasting them into the actual application form. <a href="#" class="open-downloads-form">Visit our Downloads area to find these useful documents</a>.</p>

		<h4>Multiple Applications</h4>

		<p>We welcome you to submit as many applications as you’d like. Each application is for one project, product, service, company, organization, etc. in one category. You can submit the same thing for multiple categories and/or submit multiple things for the same category, but each will be a separate application.</p>
		
		<h4>Responding to Questions</h4>
		
		<p>You are free to decide how long to make your responses to the questions on the application&mdash;<strong>there are no word limits or minimums</strong>&mdash;and all of the questions are optional. The application questions are designed to be simple and open-ended, allowing you to respond however you see fit.</p>
		
		<p>As well, we invite you to provide whatever information and materials you already have readily available instead of creating new content for the application. You are of course welcome to specially tailor your responses to this award application, but write-ups and materials prepared for other purposes are perfectly acceptable if they can help us understand the important aspects of your product, project, service, solution, team, etc.</p>
		
		<p>Think of this application as a means of introducing us to your product, service, company, team, etc., and as the beginning of a conversation. If your application passes initial screening and the judges indicate they're interested in learning more, the DfE representative assigned to you will work with you to add greater detail to your application to improve its chances of reaching the final round.</p>
		
		<h4>Supporting Files and Media</h4>
		
		<p><strong>You are not required to submit videos, presentations, images, or other media to accompany your application.</strong> Nevertheless, it will often be helpful for you to provide screenshots, images, deliverables, case studies, documents, and other such supporting materials to support your application. The award application form includes a place where you can provide web addresses for any files that you’d like to have accompany your application. We understand that often these materials are not meant for public dissemination, so they will be accessible only by DfE staff and the judges unless you give us explicit permission to use them in a UX Magazine article or on the awards showcase site.</p>

		<p>Because of file size and storage capacity limitations, we are unable to accept uploads directly from the application form. Instead, we ask that you upload materials to your company’s web server or to a file sharing service such as Dropbox, Box, Google Drive, or other similar services. If you need to assign access privileges to a specific email address, please use awardapps@designforexperience.com. You can list up to 25 links on the award application, and you can also package multiple files into a ZIP file.</p>
		
		<h3>Application Contents</h3>
		
		<p>The application consists of <a href="javascript:openAppForm()">a simple, one-page, web-based form</a> that's divided into three brief sections:</p>
		<ol>
			<li><strong>Applicant Information:</strong> the name of the entity or individual submitting the application, and contact info for the person responsible for the application.</li>
			<li><strong>Application Information:</strong> two questions to help us understand how to handle your application. This is where you’ll indicate which category you’re applying for, but if you're uncertain which category is the best fit for you, just choose the <em>"Unsure / Help Me Decide"</em> option. You'll still be able to complete the application, and a DfE representative will follow up with you to help you decide which categories are the best fit.</li>
			<li><strong>Questions:</strong> three general questions about the company, product, service, organization, team, etc., that’s the subject of the application.</li>
		</ol>
		
