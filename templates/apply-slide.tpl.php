		<h3>Dates and Fees</h3>
		
		<p>This year's program is a retrospective on the best of UX in 2014, so the application window opened on November 21st and the <strong>last day to apply is February 28th</strong>. We will be evaluating applications continuously during the application window and beyond, and if our judges have any questions we will contact you to ask follow-up questions until <strong>winners are announced on March 25th</strong>.</p>

		<p>In last year's program, nearly all of the applications came in within a couple days of the deadline, and we were compelled to extend the deadline to accomodate a large number of late applicants. We're hoping to avoid doing that again to ensure our judging process is smooth and on-schedule, so we've created a <strong>tiered application fee structure</strong> to provide an incentive to apply ahead of the final deadline. The <strong>base application fee is $245</strong>, and the fee increases according to this schedule:</p>
		
